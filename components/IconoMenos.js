const IconoMenos = ({ cantidad, setCantidad }) => {
  return (
    <div>
      <button
        onClick={() => {
          if (cantidad <= 1) return;
          setCantidad(cantidad - 1);
        }}
      >
        <svg
          xmlns="http://www.w3.org/2000/svg"
          className="h-7 w-7"
          fill="none"
          viewBox="0 0 24 24"
          stroke="currentColor"
          strokeWidth={2}
        >
          <path
            strokeLinecap="round"
            strokeLinejoin="round"
            d="M15 12H9m12 0a9 9 0 11-18 0 9 9 0 0118 0z"
          />
        </svg>
      </button>
    </div>
  );
};

export default IconoMenos;
